package Capitulo07.bloque04;

public class NoValidNumberEvent {
	private int number;
	private String nonNumberValue;
	/**
	 * @return the number
	 */
	public int getNumber() {
		return number;
	}
	/**
	 * @param number the number to set
	 */
	public void setNumber(int number) {
		this.number = number;
	}
	/**
	 * @return the nonNumberValue
	 */
	public String getNonNumberValue() {
		return nonNumberValue;
	}
	/**
	 * @param nonNumberValue the nonNumberValue to set
	 */
	public void setNonNumberValue(String nonNumberValue) {
		this.nonNumberValue = nonNumberValue;
	}
	
	
	
}
