package Capitulo05.Prueba;

public class Coche {
	/**
	 * Propiedades de la clase Coche
	 */
	private String name;
	private String model;
	private String height;
	private float price;
	private int velocity;
	
	/**
	 * Metodo constuctor por defecto.
	 */
	public Coche() {
	}
	
	/**
	 * Metodo constructor.
	 * @param name
	 * @param model
	 * @param height
	 * @param price
	 * @param valocity
	 */
	public Coche(String name, String model, String height, float price, int valocity) {
		this.name = name;
		this.model = model;
		this.height = height;
		this.price = price;
		this.velocity = velocity;
	}
	
	
	
	
	
	
	
	
}
